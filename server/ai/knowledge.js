export function matchKnowledge(query) {
  const q = query.toLowerCase();

  for (const [key, qa] of Object.entries(DENTAL_KNOWLEDGE.commonQuestions)) {
    for (const pattern of qa.patterns) {
      if (q.includes(pattern.toLowerCase())) {
        return { type: 'answer', key, response: qa.response, source: qa.source };
      }
    }
  }

  for (const [category, items] of Object.entries(DENTAL_KNOWLEDGE)) {
    if (category === 'commonQuestions') continue;
    for (const [name, info] of Object.entries(items)) {
      if (info.keywords) {
        for (const kw of info.keywords) {
          if (q.includes(kw.toLowerCase())) {
            return { type: 'topic', category, name, context: info.context, description: info.context };
          }
        }
      }
      if (info.topics) {
        for (const topic of info.topics) {
          if (q.includes(topic.toLowerCase())) {
            return { type: 'specialty', name, description: info.description, topics: info.topics };
          }
        }
      }
    }
  }

  return null;
}

export default { DENTAL_KNOWLEDGE, matchKnowledge };
