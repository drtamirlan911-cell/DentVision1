export const DENTAL_ICD10_SEED: Icd10SeedRow[] = TITLES.map(([code, description]) => ({
  code,
  description,
  category: catFor(code),
}));

export function mapIcd10Row(row: { code: string; description: string; category: string | null }) {
  return {
    code: row.code,
    name: row.description,
    description: row.description,
    category: row.category || catFor(row.code),
  };
}

export function searchDentalCatalog(q?: string, limit = 200) {
  const needle = String(q || '').trim().toLowerCase();
  const rows = !needle
    ? DENTAL_ICD10_SEED
    : DENTAL_ICD10_SEED.filter(
        (r) =>
          r.code.toLowerCase().includes(needle) ||
          r.description.toLowerCase().includes(needle) ||
          r.category.toLowerCase().includes(needle),
      );
  return rows.slice(0, limit).map(mapIcd10Row);
}
