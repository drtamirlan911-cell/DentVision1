/**
 * Built-in dental ICD-10 catalog used to seed DB and as API fallback.
 * Schema stores title in `description`; API maps it to `name` for the UI.
 */
export type Icd10SeedRow = {
  code: string;
  description: string;
  category: string;
};

function catFor(code: string): string {
  const root = code.split('.')[0] || code;
  if (root.startsWith('K02')) return 'Кариес';
  if (root.startsWith('K03')) return 'Твёрдые ткани зубов';
  if (root.startsWith('K04')) return 'Пульпа и периодонт';
  if (root.startsWith('K05') || root.startsWith('K06')) return 'Пародонтология';
  if (root.startsWith('K07') || root.startsWith('M26')) return 'Ортодонтия';
  if (root.startsWith('K08') || root.startsWith('K09') || root.startsWith('K10')) return 'Хирургия / челюсти';
  if (root.startsWith('K12') || root.startsWith('K13') || root.startsWith('K14')) return 'Слизистая / язык';
  if (root.startsWith('S02') || root.startsWith('S09')) return 'Травмы';
  if (root.startsWith('Z')) return 'Обследование / протезирование';
  return 'Стоматология';
}

