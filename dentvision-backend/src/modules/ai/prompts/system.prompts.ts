export function rolePromptFor(role: string): string {
  const key = String(role || '').toLowerCase();
  if (key === 'руководитель') return ROLE_PROMPTS.owner;
  if (key === 'администратор') return ROLE_PROMPTS.admin;
  return ROLE_PROMPTS[key] || '';
}
